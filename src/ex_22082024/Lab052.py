# While Loop
# In Python i++, i-- -> We don't need increment and decrement in Python
i = 0
while i < 5:
    print(i, end=" ")
    i = i + 1
# it will print last value also if we use = operator also.

j = 0
while j <= 5:
    print(j, end=" ")
    j = j + 1
# Note: Python allows to reuse the same variable so we can use i also on the place of j.
