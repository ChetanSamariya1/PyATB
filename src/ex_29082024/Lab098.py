# Conversion List to Tuple
t1 = tuple(["Rahul", "Abhishek", "Ankita"])
print(t1)

hero1 = ("Batman", "Bruce Wayne")
hero2 = ("Wonder Woman", "Diana Prince")

new_tuple = (hero1,hero2)
print(new_tuple)
print(new_tuple[0])
print(new_tuple[0][0])
print(new_tuple[0][1])
print(new_tuple[1][0])
print(new_tuple[1][1])