# Operators

# Not Operator -> It reverse the value
# It will work with Boolean only.
is_age_30 = True

print(not is_age_30)
# print(!(is_age_30)) -> It won't work in Python. ! is concept of Java

# Is operator

# Logical Operators ->  Boolean
# <, >, <=, >=,

a = 15
b = 20

print(a > b)
print(a < b)
print(a <= b)
print(a >= b)
print(a == b)

# OR Gate & AND Gate
x = True
y = False
print(x or y)
print(x and y)